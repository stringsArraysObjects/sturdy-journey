//PARAMETERS:   none
//RESULT:       given an array of integers, return
//              a new array with each value doubled
//EXAMPLE:      [1, 2, 3] --> [2, 4, 6]
//PSEUDOCODE:   Will use the map The map() method of 
//              Array instances creates a new array populated 
//              with the results of calling a provided function
//              on every element in the calling array.

function maps(x){
    return x.map(n => n * 2);
  }